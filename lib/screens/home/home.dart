        ),
      ),
    );
  }

  Widget getCategoryUI() {
    return Column(
      mainAxisAlignment: MainAxisAlignment.center,
      crossAxisAlignment: CrossAxisAlignment.start,
      children: <Widget>[
        Padding(
          padding: const EdgeInsets.only(top: 8.0, left: 18, right: 16),
          child: Text(
            'Category',
            textAlign: TextAlign.left,
            style: TextStyle(
              fontWeight: FontWeight.w600,
              fontSize: 22,
              letterSpacing: 0.27,
              color: DesignCourseAppTheme.darkerText,
            ),
          ),
        ),
        const SizedBox(
          height: 16,
        ),
        Padding(
          padding: const EdgeInsets.only(left: 16, right: 16),
          child: Row(
            children: <Widget>[
              getButtonUI(CategoryType.one, categoryType == CategoryType.one),
              const SizedBox(
                width: 16,
              ),
              getButtonUI(
                  CategoryType.two, categoryType == CategoryType.two),
              const SizedBox(
                width: 16,
              ),
              getButtonUI(
                  CategoryType.three, categoryType == CategoryType.three),
            ],
          ),
        ),
        const SizedBox(
          height: 16,
        ),
        CategoryListView(
          
        ),
